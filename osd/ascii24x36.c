void LCDDisplayASCII8X16(u32 x0, u32 y0, u32 ForeColor, char * s)
{
	u32 i,j,k,x,y,xx;
	u8 qm;
	u32 ulOffset;
	u32 ywbuf[36],temp[2];
	u32 tmp;
    
	for(i = 0; i < strlen((const char*)s); i++)
	{
		if(((u8)*(s+i) >= 127) || (((u8)*(s+i) <= 31)))
		{
			temp[0] = *(s + i);
			temp[1] = '\0';
			return;
		}
		else
		{
			qm = *(s+i);
			qm -= 0x20;
    		ulOffset = (u32)(qm) * 108;	
    		for (j = 0; j < 36; j ++)
    		{
			tmp = j*3;
    			ywbuf[j] = (g_ucAscii8x16[ulOffset + tmp] << 16) |  (g_ucAscii8x16[ulOffset + tmp + 1] << 8) | g_ucAscii8x16[ulOffset + tmp + 2];
    		}

    		for(y = 0; y < 36; y++)
    		{
       		    for(x = 0; x < 24; x++) 
       		    {
           			k = x % 24;
	       			if (ywbuf[y]  & (0x800000 >> k))
	       			{
	       				xx = x0 + x + i*24;
	       				PutTFT16Bit_480640( xx, y + y0, ForeColor);
	       			}
	   		    }
    	    }
         }
    }
}
